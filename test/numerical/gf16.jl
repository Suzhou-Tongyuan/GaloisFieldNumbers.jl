        12243 15585 16528 11900 33326 34867 15914 31343
        40768 27643 6351 27849 54542 4349 2805 43378
    ]
    @test all(iszero, rem.(X, Y))

    @test inv.(X) .* X == fill(T(1), 8, 8)
    @test inv(X) * X == [i == j ? one(T) : zero(T) for i in 1:8, j in 1:8]

    X = GF{16,69643,UInt16}[53, 150, 148, 16400, 184, 127, 176]
    Y = GF{16,69643,UInt16}[76, 198, 184, 25000, 39, 190, 41]
    convref = GF{16,69643,UInt16}[
        3132,
